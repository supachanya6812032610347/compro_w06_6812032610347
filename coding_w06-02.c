#include <stdio.h>

int main() {
    // ส่วนของโปรแกรม w06-02-01
    int i = 1, j = 2, k; // กำหนดค่าเริ่มต้น i=1, j=2

    k = i + j;           // บรรทัดที่ 2: k = 1 + 2 = 3
    i = i + (k * j);     // บรรทัดที่ 3: i = 1 + (3 * 2) = 7
    j = i / 2;           // บรรทัดที่ 4: j = 7 / 2 = 3 (หารจำนวนเต็ม)
    k = i % 2;           // บรรทัดที่ 5: k = 7 % 2 = 1
    i = (j + k) * 3;     // บรรทัดที่ 6: i = (3 + 1) * 3 = 12

    printf("ผลลัพธ์ w06-02-01:\n");
    printf("i = %d\n", i);
    printf("j = %d\n", j);
    printf("k = %d\n\n", k);

    // ส่วนของโปรแกรม w06-02-02
    double x = 1.0, y = 2.0, z; // กำหนดค่าเริ่มต้น x=1.0, y=2.0

    x = y + 5.0;               // บรรทัดที่ 2: x = 2.0 + 5.0 = 7.0
    y = x / 2.0;               // บรรทัดที่ 3: y = 7.0 / 2.0 = 3.5
    y = (x * 3.0) + 4.0;       // บรรทัดที่ 4: y = (7.0 * 3.0) + 4.0 = 25.0
    x = -0.5 - y;              // บรรทัดที่ 5: x = -0.5 - 25.0 = -25.5
    z = x + y;                 // บรรทัดที่ 6: z = -25.5 + 25.0 = -0.5

    printf("ผลลัพธ์ w06-02-02:\n");
    printf("x = %.1f\n", x);
    printf("y = %.1f\n", y);
    printf("z = %.1f\n", z);

    return 0;
}