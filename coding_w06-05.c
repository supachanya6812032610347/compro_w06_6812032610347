#include <stdio.h>

int main() {
    // คำนวณนิพจน์แต่ละตัวและแสดงผลลัพธ์

    // A = -2 + 5 * 2;
    // การคูณ (*) มีลำดับก่อนการบวก (+)
    int A = -2 + 5 * 2; // 5*2=10, -2+10=8

    // B = 10/2 * 3;
    // การหาร (/) และคูณ (*) มีลำดับเท่ากันและจะประเมินจากซ้ายไปขวา
    int B = 10 / 2 * 3; // 10/2=5, 5*3=15

    // C = 6 / 2 + 3 * (4 % 2);
    // วงเล็บ () มีลำดับสูงสุด, % คือโมดูลัส (หารเอาเศษ)
    // 4 % 2 = 0, 6/2=3, 3*0=0, 3+0=3
    int C = 6 / 2 + 3 * (4 % 2);

    // D = (5 + 2) * 15 % 4;
    // คำนวณในวงเล็บก่อน, * และ % มีลำดับเท่ากันและประเมินจากซ้ายไปขวา
    // (5+2)=7, 7*15=105, 105%4 = 1 (105 หาร 4 เหลือเศษ 1)
    int D = (5 + 2) * 15 % 4;

    // E = 6 + 2 * 2 - 6 / 2;
    // คูณและหารก่อน, จากนั้นบวกและลบจากซ้ายไปขวา
    // 2*2=4, 6/2=3, 6+4=10, 10-3=7
    int E = 6 + 2 * 2 - 6 / 2;

    // F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    // คำนวณ * / % ก่อน, จากนั้นบวกและลบจากซ้ายไปขวา
    // 3*2=6, 8/4=2, 6%5=1, 5+6=11, 11-2=9, 9+1=10
    int F = 5 + 3 * 2 - 8 / 4 + (6 % 5);

    // G = (4 + 3) * 2 - 10 / (2 + 3);
    // คำนวณในวงเล็บก่อน, คูณและหารก่อน, บวกและลบหลังสุด
    // (4+3)=7, (2+3)=5, 7*2=14, 10/5=2, 14-2=12
    int G = (4 + 3) * 2 - 10 / (2 + 3);

    // แสดงผลลัพธ์ทั้งหมด
    printf("A = %d\n", A);
    printf("B = %d\n", B);
    printf("C = %d\n", C);
    printf("D = %d\n", D);
    printf("E = %d\n", E);
    printf("F = %d\n", F);
    printf("G = %d\n", G);

    return 0;
}
