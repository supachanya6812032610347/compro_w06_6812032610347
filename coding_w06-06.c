#include <stdio.h>

int main() {
    int a = 5, b = 2;
    float x = 3.0, y = 4.5;

    // r1: ใช้ค่า a ก่อนแล้วค่อยเพิ่ม (++ เป็น post-increment)
    // a++ * b = 5 * 2 = 10
    // (int)y = (int)4.5 = 4 → 4 % 3 = 1
    // ผลรวม: 10 + 1 = 11
    int r1 = a++ * b + (int)y % 3;

    // ตอนนี้ a = 6 (จากการเพิ่มในบรรทัดก่อนหน้า)
    // (a > b) = (6 > 2) = true (1)
    // (int)x = 3, 3 / b = 3 / 2 = 1 → 1 < 2 = true (1)
    // true && true = true → 1
    int r2 = (a > b) && ((int)x / b < 2);

    // ++x = 4.0 (เพิ่มก่อนใช้)
    // 4.0 * y = 4.0 * 4.5 = 18.0
    // a / 2 = 6 / 2 = 3 (หารแบบจำนวนเต็ม)
    // 18.0 - 3 = 15.0
    float r3 = ++x * y - a / 2;

    // x += 1.5 → x = 5.5
    // (x > y) = (5.5 > 4.5) = true (1)
    // เนื่องจากฝั่งซ้ายของ || เป็น true แล้ว ฝั่งขวาไม่ถูกประเมิน
    // ผลลัพธ์คือ true (1.00)
    float r4 = ((x += 1.5) > y) || (b-- > 0);

    // แสดงผลลัพธ์
    printf("r1 = %d\n", r1);     // 11
    printf("r2 = %d\n", r2);     // 1
    printf("r3 = %.2f\n", r3);   // 15.00
    printf("r4 = %.2f\n", r4);   // 1.00

    return 0;
}